
public class Operator01 {
	
	public static void main(String[] args) {
		int num1 = 10;
		int num2 = 6;
		System.out.println(num1 + num2);
		System.out.println(num1 - num2);
		System.out.println(num1 * num2);
		System.out.println(num1 / num2);
		// 정수 간 연산 결과는 항상 정수로 반환
		System.out.println(num1 % num2);
		
		System.out.println(num1 % 2 == 0);
		System.out.println(num1 % 2 != 0);
	}

}
